function meu_callback(conteudo) {
    const showBairro = document.getElementById('valueBairroEl');
    let bairro = conteudo.bairro;

    let colectBairro = bairros.find(b => b.name == bairro);

    if (colectBairro) {
        showBairro.innerHTML = `<small class="sucesscep"><i class="far fa-check-square"></i> CEP encontrado!</small> <br> A coleta seletiva no bairro <strong>${colectBairro.name}</strong> é:<br><br><strong>Dia:</strong> ${colectBairro.date}<br><strong>Horas:</strong> ${colectBairro.hour}<br>Atenção: ${colectBairro.obs}`
    } else {
        showBairro.innerHTML = `<small class="failcep"><i class="fas fa-exclamation-triangle"></i> Ops!!!</small> <br>Não encontramos o seu cep ou a coleta seletiva no seu bairro, digite o cep de um bairro mais próximo`;
    }


};

function pesquisacep(valor) {
    let cep = valor;
    //Cria um elemento javascript.
    let script = document.createElement('script');
    //Sincroniza com o callback.
    script.src = 'https://viacep.com.br/ws/' + cep + '/json/?callback=meu_callback';
    //Insere script no documento e carrega o conteúdo.
    document.body.appendChild(script);
};